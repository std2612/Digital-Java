package Day2;

public class ForEx1 {

	public static void main(String[] args) {
		// for문 기본 예제 : Hello World! 5번 출력
		
		int i;
		for(i=1; i<=5; i++) {
			System.out.println("Hello World!" + i);
		}
		
		System.out.println("-------------");
		for(i=0; i<5; i++) {
				System.out.println("Hello World!" + i);
		}
		System.out.println("-------------");
		//for문 기본 예제 : 1부터 5까지 출력하는 예제
		for(i=1; i<=5; i++) {
			System.out.println(i);
		}		
	}

}
